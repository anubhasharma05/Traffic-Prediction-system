#  Advanced Traffic Prediction System

import pandas as pd
import numpy as np
import matplotlib.pyplot as plt

from sklearn.model_selection import train_test_split
from sklearn.ensemble import RandomForestClassifier
from sklearn.metrics import accuracy_score, classification_report

# -------------------------------
# STEP 1: Generate Realistic Dataset
# -------------------------------

np.random.seed(42)

rows = 1000

data = {
    "hour": np.random.randint(0, 24, rows),
    "day": np.random.randint(0, 7, rows),  # 0 = Sunday
    "weather": np.random.choice(["Clear", "Rain", "Fog"], rows),
    "road_type": np.random.choice(["Highway", "City"], rows),
}

df = pd.DataFrame(data)

# -------------------------------
# STEP 2: Create Traffic Logic
# -------------------------------

def assign_traffic(row):
    if (row["hour"] in range(8, 11) or row["hour"] in range(17, 20)) and row["day"] < 5:
        return "High"
    elif row["weather"] == "Rain":
        return "Medium"
    else:
        return "Low"

df["traffic"] = df.apply(assign_traffic, axis=1)

# -------------------------------
# STEP 3: Encode Data
# -------------------------------

df_encoded = pd.get_dummies(df, columns=["weather", "road_type"], drop_first=True)

X = df_encoded.drop("traffic", axis=1)
y = df["traffic"]

# -------------------------------
# STEP 4: Train Model
# -------------------------------

X_train, X_test, y_train, y_test = train_test_split(X, y, test_size=0.2)

model = RandomForestClassifier(n_estimators=100)
model.fit(X_train, y_train)

# -------------------------------
# STEP 5: Evaluate Model
# -------------------------------

y_pred = model.predict(X_test)

print("\n📊 Model Accuracy:", accuracy_score(y_test, y_pred) * 100)
print("\n📄 Classification Report:\n", classification_report(y_test, y_pred))

# -------------------------------
# STEP 6: Visualization
# -------------------------------

traffic_counts = df["traffic"].value_counts()
traffic_counts.plot(kind="bar")
plt.title("Traffic Distribution")
plt.xlabel("Traffic Level")
plt.ylabel("Count")
plt.show()

# -------------------------------
# STEP 7: User Prediction
# -------------------------------

print("\n🚦 Traffic Prediction System")

hour = int(input("Enter hour (0-23): "))
day = int(input("Enter day (0=Sunday ... 6=Saturday): "))
weather = input("Enter weather (Clear/Rain/Fog): ")
road = input("Enter road type (Highway/City): ")

input_df = pd.DataFrame({
    "hour": [hour],
    "day": [day],
    "weather": [weather],
    "road_type": [road]
})

input_encoded = pd.get_dummies(input_df)
input_encoded = input_encoded.reindex(columns=X.columns, fill_value=0)

prediction = model.predict(input_encoded)

print("\n Predicted Traffic Level:", prediction[0])